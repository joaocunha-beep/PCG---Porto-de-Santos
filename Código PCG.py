# =========================================================
# GRÁFICO DA MÉDIA HISTÓRICA - CORRIGIDO
# =========================================================

# =========================================================
# IMPORTAÇÕES
# =========================================================
import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns
import os
import glob

# =========================================================
# CONFIGURAÇÃO VISUAL
# =========================================================
sns.set_theme(style="whitegrid")

plt.rcParams['figure.figsize'] = (16,8)

# =========================================================
# PASTA DOS EXCEL
# =========================================================
pasta = "/content/dados_porto/2023"

# =========================================================
# PEGAR XLSX
# =========================================================
arquivos = glob.glob(os.path.join(pasta, "*.xlsx"))

# =========================================================
# VERIFICAÇÃO
# =========================================================
if len(arquivos) == 0:
    print("NENHUM EXCEL ENCONTRADO")
    exit()

# =========================================================
# PEGAR PRIMEIRO ARQUIVO
# =========================================================
arquivo = arquivos[0]

print(f"\nLENDO:\n{arquivo}")

# =========================================================
# LER PLANILHA
# =========================================================
df = pd.read_excel(arquivo)

# =========================================================
# REMOVER LINHAS VAZIAS
# =========================================================
df = df.dropna(how='all')

# =========================================================
# MOSTRAR COLUNAS
# =========================================================
print("\nCOLUNAS:")
print(df.columns)

# =========================================================
# PRIMEIRA COLUNA = ANO
# =========================================================
col_ano = df.columns[0]

# =========================================================
# ÚLTIMA COLUNA = MÉDIA
# =========================================================
col_media = df.columns[-1]

# =========================================================
# PEGAR APENAS COLUNAS NECESSÁRIAS
# =========================================================
df = df[[col_ano, col_media]]

# =========================================================
# CONVERTER ANO
# =========================================================
df[col_ano] = pd.to_numeric(
    df[col_ano],
    errors='coerce'
)

# =========================================================
# CONVERTER MÉDIA
# =========================================================
df[col_media] = pd.to_numeric(
    df[col_media],
    errors='coerce'
)

# =========================================================
# REMOVER NULOS
# =========================================================
df = df.dropna()

# =========================================================
# MOSTRAR DADOS
# =========================================================
print("\nDADOS UTILIZADOS:")
print(df)

# =========================================================
# CRIAR PASTA RESULTADOS
# =========================================================
os.makedirs("resultados", exist_ok=True)

# =========================================================
# CRIAR GRÁFICO
# =========================================================
plt.figure(figsize=(16,8))

sns.lineplot(
    data=df,
    x=col_ano,
    y=col_media,
    marker="o",
    linewidth=3,
    color="darkgreen"
)

# =========================================================
# VALORES NOS PONTOS
# =========================================================
for x, y in zip(df[col_ano], df[col_media]):

    plt.text(
        x,
        y,
        f"{y/1_000_000:.1f} milhões t",
        ha='center',
        va='bottom',
        fontsize=9
    )

# =========================================================
# TÍTULOS
# =========================================================
plt.title(
    "Média Histórica - Porto de Santos",
    fontsize=20,
    weight='bold'
)

plt.xlabel("Ano", fontsize=14)
plt.ylabel("Toneladas", fontsize=14)

# =========================================================
# MOSTRAR TODOS OS ANOS
# =========================================================
plt.xticks(
    df[col_ano],
    rotation=45
)

# =========================================================
# AJUSTES
# =========================================================
plt.tight_layout()

# =========================================================
# SALVAR
# =========================================================
saida = "resultados/grafico_media_historica_corrigido.png"

plt.savefig(
    saida,
    dpi=300,
    bbox_inches='tight'
)

# =========================================================
# MOSTRAR
# =========================================================
plt.show()

# =========================================================
# FINALIZAÇÃO
# =========================================================
print("\n===================================")
print("GRÁFICO GERADO COM SUCESSO")
print(f"SALVO EM:\n{saida}")
print("===================================")